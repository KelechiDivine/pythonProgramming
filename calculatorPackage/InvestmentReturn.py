principal = 1000
rate = 7 / 100


def invest_after_ten_years(simple_interest_after_ten_years):
    time = 10
    simple_interest_after_ten_years = (principal * rate) * time
    return simple_interest_after_ten_years


def invest_after_twenty_years(simple_interest_after_twenty_years):
    time = 20
    simple_interest_after_twenty_years = (principal * rate) * time
    return simple_interest_after_twenty_years


def investment_after_thirty_years(simple_interest_after_thirty_years):
    time = 30
    simple_interest_after_thirty_years = (principal * rate) * time
    return simple_interest_after_thirty_years
