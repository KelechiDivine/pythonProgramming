class Calculator :

    def adding_two_numbers(firstNumber, secondNumber) -> int:
        sum = firstNumber + secondNumber
        return sum
    
    def subtracting_two_numbers(firstNumber, secondNumber) -> int:
        minus = firstNumber - secondNumber
        return minus
    
    def multiplying_two_numbers(firstNumber, secondNumber) -> int:
        multiplication = firstNumber * secondNumber
        return multiplication
    
    def dividing_two_numbers(firstNumber, secondNumber) -> int:
        division = firstNumber / secondNumber
        return division
    
    
