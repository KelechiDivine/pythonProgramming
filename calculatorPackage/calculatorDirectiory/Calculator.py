def adding_two_numbers(firstNumber, secondNumber):
    sum = firstNumber + secondNumber
    return sum

def subtracting_two_numbers(firstNumber, secondNumber):
    minus = firstNumber - secondNumber
    return minus

def multiplying_two_numbers(firstNumber, secondNumber):
    multiplication = firstNumber * secondNumber
    return multiplication

def dividing_two_numbers(firstNumber, secondNumber):
    division = firstNumber / secondNumber
    return division


