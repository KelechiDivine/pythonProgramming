class HeartRate:
    def first_name(self, first_name):
        self.first_name = first_name
        return first_name
    
    def second_name(self, second_name):
        self.second_name = second_name
        return second_name
    
    def month(self, month):
        self.month = month
        return month
    
    def day(self, day):
        self.day = day
        return day
    
    def year(self, year):
        self.year = year
        return year
